package bola;

public class Bola {
    int circunferencia;
    String cor;
    String marca;
    int velocidade;

    public Bola(int circunferencia, String cor, String marca, int velocidade) {
        this.circunferencia = circunferencia;
        this.cor = cor;
        this.marca = marca;
        this.velocidade = velocidade;
    }
}
